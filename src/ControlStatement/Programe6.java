package ControlStatement;

public class Programe6 {
	
//WAP for student grade (switch statement)
	
	public static void main(String[] args) {
		
		char grade = 'A';
		
		 switch(grade)
		 
		 {
		 case'A':
			 System.out.println("Student have grade A");
			 break;
			 
		 case'B': 
			 System.out.println("Student have grade B");
			 break;
			 
		 case'C': 
			 System.out.println("Student have grade C");
			 break;
			 
		 case'D':
			 System.out.println("Student have grade D");
			 break;
			 
			 default:
				 System.out.println("Wrong input");
			 
		 }
	}
}
			 
			 
		 
			

	


